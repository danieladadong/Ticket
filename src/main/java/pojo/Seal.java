package pojo;

public class Seal {
	private String car_num;
	private int count_seal_trips;
	private int	re_trips;
	private int change_trips;
	private int seal;
	public String getCar_num() {
		return car_num;
	}
	public void setCar_num(String car_num) {
		this.car_num = car_num;
	}
	public int getCount_seal_trips() {
		return count_seal_trips;
	}
	public void setCount_seal_trips(int count_seal_trips) {
		this.count_seal_trips = count_seal_trips;
	}
	public int getRe_trips() {
		return re_trips;
	}
	public void setRe_trips(int re_trips) {
		this.re_trips = re_trips;
	}
	public int getChange_trips() {
		return change_trips;
	}
	public void setChange_trips(int change_trips) {
		this.change_trips = change_trips;
	}
	public int getSeal() {
		return seal;
	}
	public void setSeal(int seal) {
		this.seal = seal;
	}
	public Seal() {
		
	}

}
